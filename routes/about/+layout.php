<h1>About Monarch</h1>

<p>The goal behind Monarch is simple - try to find the simplest way to enable developers to make a web site or
    application. This should enable them to quickly build out UIs with the features and speed users expect. It should
    provide the simplest version of the tools that will get the job done.</p>

